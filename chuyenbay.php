<script>
    $(document).ready(function() {
        $("#list_dep_ticket .ticket_item .ticket_info .detail").click(function() {
            if (parseInt($(this).attr("value"))) {
                $(this).html('Chi tiết <i class="fa fa-angle-down" aria-hidden="true"></i>');
                $(this).attr("value", 0);
            } else {
                $(this).html('Ẩn <i class="fa fa-angle-up" aria-hidden="true"></i>');
                $(this).attr("value", 1);
            }
            $(this).parent().next(".flight_info").slideToggle(1000);
        });

        $("#list_dep_ticket .ticket_item .ticket_info .btn_select").click(function() {
            var list = $("#list_dep_ticket .ticket_item");

            if (parseInt($(this).attr('edit'))) {
                $(this).parent().parent(".ticket_item").attr("select", 0);
                $(".dedail_and_total .continue").attr('select', 0);

                $(this).html('Chọn');
                $(this).css({
                    "background-color": "white",
                    "color": "red"
                });
                $(this).attr('edit', 0);
                $(".dedail_and_total .total_price").html('0<span> đ</span>');

                for (var i = 0; i < list.length; i++) {
                    $(list[i]).fadeIn();
                }


            } else {
                $(this).parent().parent(".ticket_item").attr("select", 1);
                $(this).html('Thay đổi');
                $(this).attr('edit', 1);
                $(".dedail_and_total .total_price").html($(this).parent().parent(".ticket_item").attr('price') + '<span> đ</span>');
                $(".dedail_and_total .continue").attr('select', 1);

                $(this).css({
                    "background-color": "red",
                    "color": "white"
                });
                for (var i = 0; i < list.length; i++) {
                    if (parseInt($(list[i]).attr("select"))) {

                    } else {
                        $(list[i]).fadeOut();
                    }
                }
            }


        });
    });
</script>
